trucker.acceptOffer = function (player, offerId) {
    if (!user.isLogin(player))
        return;
    if (user.has(player, 'isStartTrucker')) {
        trucker.notify(player, 'Вы уже выполняете заказ', '~r~Ошибка');
        return;
    }
    if (trucker.has('accept' + offerId)) {
        trucker.notify(player, 'Данный заказ уже был принят', '~r~Ошибка');
        return;
    }

    let offer = trucker.getOffer(offerId);
    if (offer == null) {
        trucker.notify(player, 'Данный заказ уже был принят', '~r~Ошибка');
        return;
    }

    try {
        trucker.set('accept' + offerId, user.getId(player));
        trucker.notify(player, `~y~Принял заказ ~s~#${offerId}.\n~y~Заказчик:~s~ ${offer[2]}\nЗаберите груз в указанной точке`, `Статус заказа`);
        user.set(player, 'isStartTrucker', true);

        if (offer.length == 10)
            player.call('client:trucker:acceptOffer1', offer);
        else
            player.call('client:trucker:acceptOffer2', offer);
        //trucker.removeOffer(offerId);
    }
    catch (e) {
        methods.debug(e);
    }
};

